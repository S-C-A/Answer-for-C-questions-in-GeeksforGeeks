//The program to add two numbers together.
#include <stdio.h>

int main(){

    int a; // Variable for the numbers.
    int b;    

    printf("Please enter the first number: ");
    scanf("%d", &a); //Gets an integer value from the user and stores it inside "a".

    printf("Please enter the second number: ");
    scanf("%d", &b); //Gets an integer value from the user and stores it inside "b".

    printf("The sum of the numbers is: %d", a+b); //Prints the sum of the number.




}