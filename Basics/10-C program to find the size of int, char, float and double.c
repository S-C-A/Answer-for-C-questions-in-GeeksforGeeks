// C program to find the size of int, char, float and double.

#include <stdio.h>
 
int main()
{
    int a;
    char b;
    float c;
    double d;
 

    printf("Size of int is: %d\n", sizeof(a));
 
    printf("Size of char is: %d\n", sizeof(b));
 
    printf("Size of float is: %d\n", sizeof(c));
  
    printf("Size of double is: %d", sizeof(d));
 

}